export class LibUser {

    FirstName:string;
    LastName:string;
    Email:string;
    RoleId:string;
    Gender:string;
    City:string;
    Contact:string;
    Password:string;

constructor(FirstName:string,LastName:string,Email:string,RoleId:string,Gender:string,City:string,Contact:string,Password:string){
    this.FirstName=FirstName;
    this.LastName=LastName;
    this.Email=Email;
    this.RoleId=RoleId;
    this.Gender=Gender;
    this.City=City;
    this.Contact=Contact;
    this.Password=Password;
}

}
